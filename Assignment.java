/* Assignment Operator Example..!

It is also unary operator so can work on single
operand. There are two types
Type1 = Simple
Type2 = compound


*/

class Assignment{

	public static void main(String[] args) {
		
		int a;
		a=10;  //simple 

		System.out.println(a);

		a+=10; //compound

		System.out.println(a);

		a-=10; //compound

		System.out.println(a);

	}
	
}