/* do while loop is a post test loop
here it executes statement first then it test the condition
It is used when we want to executes loop body atleast once.
even condition is false.
It is also known as exit control loop.


*/

class DoWhile{
	
	public static void main(String[] args) {
		int n=1;
		do
		{
			System.out.print(n);
		}
		while(n<0);
	}
}

